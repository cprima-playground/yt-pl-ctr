"""Build per-episode feature files from cached transcripts.

Pipeline per transcript:
  1. Strip YouTube auto-caption noise ([Music], [Applause], uh, um, ...)
  2. Lowercase + tokenize on word boundaries
  3. Remove stopwords (NLTK English list)
  4. Stem with Porter stemmer
  5. Compute token frequency

Output: episodes/{id}/features.json
  {
    "video_id": "...",
    "token_freq": {"fight": 42, "sport": 31, ...},
    "top_tokens": [["fight", 42], ...],   // top 50
    "token_count": 1234
  }

Usage:
    uv run python scripts/build_features.py
    uv run python scripts/build_features.py --video-id MtoPEub7XwA
    uv run python scripts/build_features.py --limit 100
    uv run python scripts/build_features.py --force   # re-process existing features
"""

