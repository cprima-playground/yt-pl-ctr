# Merge all, stem, build final set
_ALL_RAW = _PRONOUNS | _BASE_VERBS | _BASE_ADVERBS | _BASE_ADJECTIVES | _SPOKEN_NOISE
_EXTRA_STEMS = {_STEMMER.stem(w) for w in _ALL_RAW}
_ALL_STOPWORDS = _STOPWORDS | _EXTRA_STEMS

# YouTube auto-caption artifacts to strip before tokenizing
_NOISE_RE = re.compile(
    r"\[(?:music|applause|laughter|inaudible|crosstalk|silence)\]"
    r"|\b(?:uh|um|uhh|umm|hmm|gonna|wanna|gotta|kinda|sorta)\b",
    re.IGNORECASE,
)
_TOKEN_RE = re.compile(r"[a-z]{3,}")  # only alpha tokens, min length 3


def process_transcript(text: str) -> dict:
    cleaned = _NOISE_RE.sub(" ", text).lower()
    tokens = _TOKEN_RE.findall(cleaned)
    tokens = [_STEMMER.stem(t) for t in tokens if t not in _ALL_STOPWORDS]
    tokens = [t for t in tokens if t not in _EXTRA_STEMS]  # post-stem filter
    freq = Counter(tokens)
    return {
        "token_freq": dict(freq.most_common()),
        "top_tokens": freq.most_common(50),
        "token_count": len(tokens),
    }


def _default_cache_dir() -> Path:
    env = os.environ.get("YT_CACHE_DIR")
    return Path(env) if env else Path(__file__).parent.parent / "cache"


def main():
    parser = argparse.ArgumentParser(description="Build feature files from cached transcripts")
    parser.add_argument("--cache-dir", type=Path, default=None)
    parser.add_argument("--limit", type=int, default=None)
    parser.add_argument("--video-id", action="append", dest="video_ids")
    parser.add_argument(
        "--force", action="store_true", help="Re-process even if features.json exists"
    )
    args = parser.parse_args()

    cache_dir = args.cache_dir or _default_cache_dir()
    if not cache_dir.exists():
        print(f"Cache not found: {cache_dir}")
        return 1

    index = cache_mod.read_index(cache_dir)
    if not index:
        print("Index is empty.")
        return 1

    if args.video_ids:
        targets = [e for e in index if e["video_id"] in args.video_ids]
    else:
        targets = [e for e in index if cache_mod.has_transcript(cache_dir, e["video_id"])]
        if not args.force:
            targets = [
                e
                for e in targets
                if not (cache_mod.episode_dir(cache_dir, e["video_id"]) / "features.json").exists()
            ]

    if args.limit:
        targets = targets[: args.limit]

    print(f"Cache: {cache_dir}")
    print(f"Processing {len(targets)} episodes")
    print()

    done = 0
    for i, entry in enumerate(targets, 1):
        vid = entry["video_id"]
        transcript = cache_mod.read_transcript(cache_dir, vid)
        if not transcript:
            continue

        result = process_transcript(transcript)
        result["video_id"] = vid

        feat_path = cache_mod.episode_dir(cache_dir, vid) / "features.json"
        feat_path.write_text(json.dumps(result, indent=2, ensure_ascii=False, encoding="utf-8"))
        done += 1

        if i % 50 == 0 or i == len(targets):
            print(f"  [{i}/{len(targets)}] {done} processed", flush=True)

    print(f"\nDone. Built features for {done} episodes.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
